package binarysearchAlgorithm;

public class Triangle {
    public static void main(String[] args) {
        int len = 7;
        for (int i = 1; i <= len; i++) {
            for (int j = 1; j <= i ; j++) {
                System.out.print("# ");
            }
            System.out.println();
        }
    }
}
